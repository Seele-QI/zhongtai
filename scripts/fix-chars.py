fixed = 0
new_lines = []

for i, line in enumerate(lines):
    if '�' not in line:
        new_lines.append(line)
        continue

    # Find text: " pattern with corruption
    # We need to find the opening quote, then the text, then �?
    # Pattern: text: "ChineseText�?  -> text: "ChineseTextCorrect"
    idx = line.find('text: "')
    if idx >= 0:
        # Find the �? after the opening quote
        start = idx + len('text: "')
        corrupt_idx = line.find('�?', start)
        if corrupt_idx >= 0:
            before = line[start:corrupt_idx]
            if before in fix_map:
                correct = fix_map[before]
                old_part = line[start:corrupt_idx + 2]  # includes �?
                line = line.replace(old_part, correct + '"', 1)
                fixed += 1
                print(f"  L{i+1}: Fixed '{before}' -> '{correct}'")
            else:
                print(f"  L{i+1}: UNKNOWN before='{before}'")
                print(f"    Full: {line.strip()[:100]}")

    new_lines.append(line)

with open(path, 'w', encoding='utf-8') as f:
    f.writelines(new_lines)

print(f"\nFixed {fixed} lines")

